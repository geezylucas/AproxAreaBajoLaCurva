    private void displayChart(XYDataset dataset0, XYDataset dataset1) {
        createChart(dataset0, dataset1);

        ChartPanel chartPanel = new ChartPanel(chart) {

            @Override
            public Dimension getPreferredSize() {
                return new Dimension(396, 408);
            }
        };
        chartPanel.setBorder(BorderFactory.createEmptyBorder(15, 15, 15, 15));
        chartPanel.setBackground(Color.white);
        add(chartPanel);

        jplChart.setLayout(new java.awt.BorderLayout());
        jplChart.add(chartPanel, BorderLayout.CENTER);
        jplChart.validate();
    }

    private void createChart(XYDataset dataset0, XYDataset dataset1) {

        JFreeChart chartDirty = ChartFactory.createXYStepAreaChart(
                "XY Area", // Chart Title
                null, // X-Axis Label
                null, // Y-Axis Label
                dataset0
        );
        XYPlot plot = (XYPlot) chartDirty.getPlot();

        XYLineAndShapeRenderer render1 = new XYLineAndShapeRenderer(true, false);
        plot.setDataset(1, dataset1);
        plot.setRenderer(1, render1);

        plot.setBackgroundPaint(Color.WHITE);

        plot.setRangeGridlinesVisible(true);
        plot.setRangeGridlinePaint(Color.BLACK);

        plot.setDomainGridlinesVisible(true);
        plot.setDomainGridlinePaint(Color.BLACK);

        chartDirty.getLegend().setFrame(BlockBorder.NONE);

        chart = new JFreeChart("Gráfica", JFreeChart.DEFAULT_TITLE_FONT, plot, true);
    }

    private double f(double x) {
        Argument varX = new Argument("x = " + x);

        Expression e = new Expression(jtfTextFunction.getText(), varX);
        double result = e.calculate();
        return result;
    }

    private void jbtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcularActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jtSalida.getModel();
        model.setRowCount(0);

        a = Double.parseDouble(jtfTextA.getText());
        b = Double.parseDouble(jtfTextB.getText());
        c = Integer.parseInt(jtfTextC.getText());
        ec = 0.5 * Math.pow(10, -c);
        redon = Math.pow(10, c + 2);
        erro[i] = 1;
        suma = 0;

        if (jrbtnRectangulos.isSelected()) {
            // Rectangulos
            n = 100;
            h = (b - a) / n;

            for (k = 0; k <= n - 1; k++) {
                suma = suma + h * f(a + k * h);
            }
        } else if (jrbtnTrapecios.isSelected()) {
            // Trapecios
            n = 10;
            h = (b - a) / n;

            for (k = 1; k <= n - 1; k++) {
                suma = suma + f(a + k * h);
            }
            suma = (h / 2) * (f(a) + f(b) + (2 * suma));
        } else if (jrbtnSimpson.isSelected()) {
            // Simpson
            n = 4;
            h = (b - a) / (2 * n);

            for (k = 1; k <= n; k++) {
                suma = suma + ((2 * f(a + (((2 * k) - 1) * h))) + f(a + (2 * k * h)));
            }
            suma = (h / 3) * (f(a) - f(b) + (2 * suma));

        }

        integra[i] = suma;
        // imprimir los renglones
        model.addRow(new Object[]{
            n,
            (Math.round(integra[i] * redon) / redon),
            (Math.round(erro[i] * redon) / redon)
        });

        while (erro[i] > ec) {
            i++;
            suma = 0;

            if (jrbtnRectangulos.isSelected()) {
                n = n + 100;
                h = (b - a) / n;

                for (k = 0; k <= n - 1; k++) {
                    suma = suma + h * f(a + k * h);
                }
            } else if (jrbtnTrapecios.isSelected()) {
                n = n + 10;
                h = (b - a) / n;

                for (k = 1; k <= n - 1; k++) {
                    suma = suma + f(a + k * h);
                }
                suma = (h / 2) * (f(a) + f(b) + (2 * suma));
            } else if (jrbtnSimpson.isSelected()) {
                n = n + 4;
                h = (b - a) / (2 * n);

                for (k = 1; k <= n; k++) {
                    suma = suma + ((2 * f(a + (((2 * k) - 1) * h))) + f(a + (2 * k * h)));
                }
                suma = (h / 3) * (f(a) - f(b) + (2 * suma));
            }

            integra[i] = suma;
            erro[i] = Math.abs((integra[i] - integra[i - 1]) / integra[i]);

            model.addRow(new Object[]{
                n,
                (Math.round(integra[i] * redon) / redon),
                (Math.round(erro[i] * redon) / redon)
            });
        }
    }//GEN-LAST:event_jbtnCalcularActionPerformed

